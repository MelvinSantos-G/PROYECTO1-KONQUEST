
package Main;


public class Nave {
    private int lugares;
    private int costoConstruir;
    private double distaRecorrida ;

    public Nave() {
    }

    public int getLugares() {
        return lugares;
    }

    public void setLugares(int lugares) {
        this.lugares = lugares;
    }

    public int getCostoConstruir() {
        return costoConstruir;
    }

    public void setCostoConstruir(int costoConstruir) {
        this.costoConstruir = costoConstruir;
    }

    public double getDistaRecorrida() {
        return distaRecorrida;
    }

    public void setDistaRecorrida(double distaRecorrida) {
        this.distaRecorrida = distaRecorrida;
    }

    
    
}
